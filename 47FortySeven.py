#Python code to create and modify a dictionary.
# creating a dictionary
person = {"name": "Yash", "age": 21, "city": "Delhi"}

# accessing values
print("Name:", person["name"])

# modifying a value
person["age"] = 22

# adding a new key-value pair
person["profession"] = "Data Scientist"

# printing updated dictionary
print("Updated Dictionary:", person)
